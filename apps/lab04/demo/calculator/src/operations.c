#include "operations.h"
#include <math.h>

double op_add(double a, double b)
{
    return a + b;
}

double op_subs(double a, double b)
{
    return a - b;
}

double op_mul(double a, double b)
{
    return a * b;
}

double op_div(double a, double b)
{
    return a / b;
}

double op_pow(double a, double b)
{
    return pow(a, b);
}

double op_sin(double stub, double x)
{
    return sin(x);
}

double op_cos(double stub, double x)
{
    return cos(x);
}
